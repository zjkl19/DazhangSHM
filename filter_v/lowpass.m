%---------------------------------------------------------------------低通滤波
f_N=48;                              %采样率  
f_p=0.01; f_s=0.5; R_p=0.5; R_s=35;   %设计要求指标 
Ws=f_s/(f_N/2); Wp=f_p/(f_N/2);      %计算归一化角频率 
[n, Wn]=buttord(Wp,Ws,R_p,R_s);      %计算阶数和截止频率 
[b,a]=butter(n, Wn);                   %计算H(z)  
% 绘制结果 
figure(1);  
x=data; 
t=1:1:15105;
subplot(2,2,1);plot(t,x,'.'); title ('输入信号'); 
y0=filter(b,a,x);  
subplot(2,2,2);plot(t,y0,'.'); title('滤波之后的信号'); 


